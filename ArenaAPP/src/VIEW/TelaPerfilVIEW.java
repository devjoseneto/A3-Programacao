    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        int confirmar = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja excluir sua conta?", "Confirmar Exclus√£o",JOptionPane.YES_NO_OPTION);
        if (confirmar == JOptionPane.YES_OPTION) {
            UsuarioService service = new UsuarioService();
            service.excluir(BancoDeDados.usuarioLogado);
            JOptionPane.showMessageDialog(rootPane, "Sua conta foi deletada");
            TelaInicialVIEW j = new TelaInicialVIEW();
            this.dispose();
            j.setVisible(true);
        }
            
    }//GEN-LAST:event_btnExcluirActionPerformed

